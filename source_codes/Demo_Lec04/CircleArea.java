public class CircleArea
{
   /** main method */
   public static void main(String[] args)
   {
      double radius;
      double area;
      
      // Assign a radius, then compute area and display the result
      radius = 20;
      area = radius * radius * 3.14159;
      System.out.println("The area for the circle of radius " +
                 radius + " is " + area);
   }
}

